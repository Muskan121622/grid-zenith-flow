import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();

    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    console.log("Processing energy optimization query with", messages.length, "messages");

    const systemPrompt = `You are an expert AI assistant for renewable energy optimization and grid management. You specialize in:

- Solar and wind energy generation forecasting
- Energy storage optimization (battery banks and pumped hydro)
- Regional demand prediction and load balancing
- Market price forecasting and dynamic bidding strategies
- Transmission loss reduction
- Grid reliability improvement
- EBITDA and profitability analysis

You have deep knowledge of:
- Indian Energy Exchange (IEX) market dynamics
- Weather-driven generation variance
- Storage charge/discharge scheduling
- Reserve capacity requirements (5% uncommitted for grid stability)
- Real-time energy allocation algorithms
- Model Predictive Control (MPC) for energy systems
- Machine learning for forecasting (time series, gradient boosting, LSTM)

Provide clear, actionable insights with:
- Quantitative analysis when possible
- Trade-offs and considerations
- Realistic parameters and assumptions
- Practical implementation guidance

Be concise but thorough. Use technical terminology appropriately while remaining accessible.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limit exceeded. Please try again later." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "AI credits depleted. Please add more credits to continue." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }

      throw new Error(`AI gateway error: ${response.status}`);
    }

    return new Response(response.body, {
      headers: {
        ...corsHeaders,
        "Content-Type": "text/event-stream",
        "Cache-Control": "no-cache",
        "Connection": "keep-alive",
      },
    });
  } catch (error) {
    console.error("Error in energy-chat function:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
